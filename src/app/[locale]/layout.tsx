import type { Metadata } from 'next'
import { GoogleAnalytics } from '@next/third-parties/google'
import { NextIntlClientProvider, useMessages } from 'next-intl'
import { ReactLenis } from 'lenis/react'

import ReduxProvider from '@/redux/ReduxProvider'

import Header from '@/components/layout/header'

import '@/styles/globals.scss'
import { helveticaRomanFont } from '../fonts'

export const metadata: Metadata = {
  title: "Moco's portfolio - Federico Desmoulin",
  description: 'Generated by create next app', // TODO: Description
}

export default function LocaleLayout({
  children,
  params: { locale },
}: Readonly<{
  children: React.ReactNode
  params: {
    locale: string
  }
}>) {
  const translations = useMessages()
  const gaId = process.env.NEXT_PUBLIC_GOOGLE_ANALYTICS_MEASUREMENT_KEY

  return (
    <html
      lang={locale}
      className={helveticaRomanFont.className}
    >
      <head></head>
      <ReactLenis
        root
        options={{
          lerp: 0.1,
          duration: 1.2,
          smoothWheel: true,
          touchMultiplier: 2,
        }}
      >
        <body>
          <Header />
          <ReduxProvider>
            <NextIntlClientProvider
              // locale={locale}
              messages={translations}
            >
              {children}
            </NextIntlClientProvider>
          </ReduxProvider>
        </body>
      </ReactLenis>
      <GoogleAnalytics gaId={gaId!} />
    </html>
  )
}

import React, { forwardRef } from 'react'
import * as THREE from 'three'
import { useGLTF } from '@react-three/drei'

import type { GLTFResult } from '@/types/data/components/three/types'

import { padsData } from '@/data/skills/rubiks_cube/three/padsData'

import { default as glbConstants } from '@/constants/assets/glbConstants.json'

type GLTFResult = GLTF & {
  nodes: {
    PadSpring: THREE.Mesh
  }
  materials: object
}

const PadSpring = forwardRef<THREE.Mesh, JSX.IntrinsicElements['mesh']>((props, ref) => {
  const { nodes } = useGLTF(glbConstants.RUBIKS_CUBE.PAD_SPRING) as GLTFResult

  return (
    <mesh
      ref={ref}
      geometry={nodes.PadSpring.geometry}
      castShadow
      receiveShadow
      {...props}
    >
      <meshStandardMaterial {...padsData.defaultValues.material} />
    </mesh>
  )
})

PadSpring.displayName = 'PadSpring'

useGLTF.preload(glbConstants.RUBIKS_CUBE.PAD_SPRING)

export default PadSpring
